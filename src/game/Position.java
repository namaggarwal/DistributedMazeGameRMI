package game;

public class Position {
	private int xPos;
	private int yPos;
	
	public void Position(){
		this.xPos = 0;
		this.yPos = 0;
	}

	public int getxPos() {
		return xPos;
	}

	public void setxPos(int xPos) {
		this.xPos = xPos;
	}

	public int getyPos() {
		return yPos;
	}

	public void setyPos(int yPos) {
		this.yPos = yPos;
	}
}
